#include "do-op.h"

void	ft_modulo(int num1, int num2)
{
	int mod;

	if (num2 == 0)
	{
		write(1, "Stop : modulo by zero\n", 22);
	}
	else
	{
		mod = num1 % num2;
		ft_putnbr(mod);
	}
}
