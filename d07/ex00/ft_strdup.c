#include <stdlib.h>

char	*ft_strcpy(char *dest, char *src)
{
	int i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(char *src)
{
	char	*dest;
	int		a;

	a = 0;
	while (src[a] != '\0')
	{
		a++;
	}
	dest = malloc(sizeof(*src) * (a + 1));
	ft_strcpy(dest, src);
	return (dest);
}
