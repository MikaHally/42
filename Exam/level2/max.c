int		max(int *tab, unsigned int len)
{
	int i;
	int j;

	j = 0;
	i = 0;
	if (len == 0)
		return (0);
	while (i < len)
	{
		if (i == 0)
			j = tab[i];
		if (tab[i] > j)
			j = tab[i];
		i++;
	}
	return (j);
}
