int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int i;

	i = 0;
	while ((*s1 || *s2) && i <= (n - 1))
	{
		i++;
		if (*s1 == *s2)
		{
			s1++;
			s2++;
		}
		else
		{
			if (*s1 > *s2)
				return (1);
			else
				return (-1);
		}
	}
	return (0);
}
