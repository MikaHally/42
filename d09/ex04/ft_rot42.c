char	*ft_rot42(char *str)
{
	while (*str)
	{
		if (*str <= 'J' && *str >= 'A')
			*str += 16;
		else if (*str <= 'Z' && *str >= 'K')
			*str = 'A' + (*str - 'K');
		else if (*str <= 'j' && *str >= 'a')
			*str += 16;
		else if (*str <= 'z' && *str >= 'k')
			*str = 'a' + (*str - 'k');
		str++;
	}
}
