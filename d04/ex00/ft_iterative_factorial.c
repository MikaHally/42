int		ft_iterative_factorial(int nb)
{
	int c;

	c = nb - 1;
	if (nb == 0)
		return (1);
	else if (nb > 0 && nb <= 12)
	{
		while (c > 1)
		{
			nb *= c;
			c--;
		}
		return (nb);
	}
	return (0);
}
