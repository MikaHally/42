char	*ft_strrev(char *str)
{
	int		a;
	int		b;
	char	temp;

	a = 0;
	while (str[a] != '\0')
	{
		a++;
	}
	a--;
	b = 0;
	while (b < a)
	{
		temp = str[a];
		str[a] = str[b];
		str[b] = temp;
		b++;
		a--;
	}
	return (str);
}
